class DomainMLPRegressor(nn.Module):
    """MLP regressor that exposes hidden features for domain alignment."""

    def __init__(self, input_dim=13, hidden_sizes=(128, 64), activation_name="relu", dropout_rate=0.1):
        super().__init__()
        layers = []
        prev_dim = input_dim
        for hidden_dim in hidden_sizes:
            layers.append(nn.Linear(prev_dim, hidden_dim))
            layers.append(get_activation(activation_name))
            if dropout_rate > 0:
                layers.append(nn.Dropout(dropout_rate))
            prev_dim = hidden_dim
        self.feature_extractor = nn.Sequential(*layers)
        self.regressor = nn.Linear(prev_dim, 1)

    def forward(self, x, return_features=False):
        features = self.feature_extractor(x)
        pred = self.regressor(features)
        if return_features:
            return pred, features
        return pred

    def extract_features(self, x):
        return self.feature_extractor(x)


